def build_vector_store(
        chunks: List[Chunk],
        model_name: str = "sentence-transformers/paraphrase-multilingual-MiniLM-L12-v2"
):
    embedder = SentenceTransformer(model_name)
    dim = embedder.get_embedding_dimension()

    user = os.getenv("ORACLE_USER", "rag_user")
    password = os.getenv("ORACLE_PASSWORD", "rag_pass")
    dsn = os.getenv("ORACLE_DSN", "172.25.208.1:1522/FREEPDB1")

    store = OracleVectorStore(user, password, dsn)
    store.ensure_table_exists(dim)
    if chunks:
        texts = [c.text for c in chunks]
        embeddings = embedder.encode(texts, show_progress_bar=True, normalize_embeddings=True)
        embeddings = np.asarray(embeddings, dtype="float32")
        print("ℹ️ Uploading chunks to Oracle AI Vector Search.")
        store.add_chunks(chunks, embeddings)
    return store, embedder


def retrieve_context(
        question: str,
        store: OracleVectorStore,
        embedder: SentenceTransformer,
        top_k: int = 8,
        min_score: float = 0.1,
        selected_doc: Optional[str] = None
) -> List[Tuple[float, Chunk]]:
    if store is None:
        return []
    q_emb = embedder.encode([question], normalize_embeddings=True)
    q_emb = np.asarray(q_emb, dtype="float32")[0]
    results = store.search(q_emb, top_k=top_k, doc_name=selected_doc)
    return [(score, chunk) for score, chunk in results if score >= min_score]


# --- LEXICAL FALLBACK ---
def normalize_for_match(text: str) -> str:
    text = unicodedata.normalize("NFD", text.lower())
    text = "".join(ch for ch in text if unicodedata.category(ch) != "Mn")
    return re.sub(r"[^a-z0-9\s]", " ", text)


def token_roots(text: str) -> List[str]:
    normalized = normalize_for_match(text)
    tokens = re.findall(r"\w+", normalized)
    roots: List[str] = []
    for token in tokens:
        if len(token) >= 6:
            roots.append(token[:6])
        elif len(token) >= 4:
            roots.append(token[:4])
        elif len(token) >= 3:
            roots.append(token)
    return roots


GENERIC_QUERY_ROOTS = {"care", "este", "ce", "cum", "se", "the", "what", "how", "tell", "is", "are"}


def meaningful_roots(text: str) -> set[str]:
    roots = set(token_roots(text))
    filtered = {root for root in roots if root not in GENERIC_QUERY_ROOTS}
    return filtered or roots


def lexical_fallback(question: str, chunks: List[Chunk], top_k: int = 6) -> List[Tuple[float, Chunk]]:
    if not chunks:
        return []
    q = question.strip().lower()
    hits: List[Tuple[float, Chunk]] = []
    target_roots = meaningful_roots(q)
    if not target_roots:
        return []
    for c in chunks:
        t = c.text.lower()
        chunk_roots = set(token_roots(t))
        overlap = 0
        for root in target_roots:
            if root in chunk_roots:
                if len(root) >= 5:
                    overlap += 2
                else:
                    overlap += 1
        if overlap > 0:
            hits.append((overlap / 10.0, c))
    hits.sort(key=lambda x: x[0], reverse=True)
    return hits[:top_k]


# --- LLM AND CHAT FORMATTING ---
def format_citations(citations: List[str]) -> str:
    unique = list(dict.fromkeys(citations))
    return " ".join(unique[:3])


class LocalCausalQA:
    def __init__(self, model_id: str = "Qwen/Qwen2.5-1.5B-Instruct", device: str = "cpu"):
        print(f"Loading tokenizer for {model_id}...")
        self.tokenizer = AutoTokenizer.from_pretrained(model_id)

        print(f"Loading causal model for {model_id}...")
        dtype = torch.float32 if device == "cpu" else torch.float16
        self.model = AutoModelForCausalLM.from_pretrained(
            model_id,
            torch_dtype=dtype,
            low_cpu_mem_usage=True
        )

        if device == "cpu":
            self.device = torch.device("cpu")
        elif device == "cuda" and torch.cuda.is_available():
            self.device = torch.device("cuda")
        elif torch.backends.mps.is_available():
            self.device = torch.device("mps")
            print("✨ Using Apple MPS (Metal) for LLM acceleration.")
        else:
            self.device = torch.device("cpu")

        print(f"Moving model to {self.device}...")
        self.model.to(self.device)
        self.model.eval()
        print("Model loaded and ready.")

    def __call__(self, prompt: str, max_new_tokens: int = 250) -> str:
        messages = [
            {"role": "system",
             "content": "You are a precise assistant. Answer strictly based on the provided context."},
            {"role": "user", "content": prompt}
        ]

        text_prompt = self.tokenizer.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=True
        )

        inputs = self.tokenizer(
            text_prompt,
            return_tensors="pt",
            truncation=True,
            max_length=2048
        ).to(self.device)

        try:
            with torch.no_grad():
                outputs = self.model.generate(
                    **inputs,
                    max_new_tokens=max_new_tokens,
                    do_sample=False,
                    pad_token_id=self.tokenizer.eos_token_id
                )
        except Exception as e:
            print(f"⚠️ Eroare la generare: {e}")
            return "A apărut o eroare la generarea răspunsului."

        input_length = inputs.input_ids.shape[1]
        generated_tokens = outputs[0][input_length:]
        text = self.tokenizer.decode(generated_tokens, skip_special_tokens=True)
        return text.strip()


def setup_llm(model_id: str = "Qwen/Qwen2.5-1.5B-Instruct", device: str = "cpu"):
    return LocalCausalQA(model_id=model_id, device=device)


def answer_question(
        question: str,
        llm,
        retrieved: List[Tuple[float, Chunk]],
        max_context_chars: int = 1200,
        conversation_history=None,
) -> str:
    context_parts = []
    used = 0
    used_sources = []

    # Adunăm fragmentele recuperate, limitate de spațiul maxim
    for score, c in retrieved:
        tag = f"[{os.path.basename(c.source)} p.{c.page}]"
        piece = f"{tag}\n{c.text}\n"
        if used + len(piece) > max_context_chars:
            break
        context_parts.append(piece)
        used += len(piece)
        used_sources.append(tag)

    context = "\n---\n".join(context_parts).strip()

    not_found_msg = "The available documents do not contain an explicit answer to your question."

    prompt = (
        f"Context information is below.\n"
        f"---------------------\n"
        f"{context}\n"
        f"---------------------\n"
        f"Given the context information and not prior knowledge, answer the query.\n"
        f"If the answer is not in the context, reply exactly with: '{not_found_msg}'\n\n"
        f"Query: {question}\n"
        f"Answer:"
    )

    out = llm(prompt, max_new_tokens=200)
    print(f"LLM output: {out}")

    clean_out = out.strip().lower()
    if (not clean_out or len(clean_out) < 5 or
            not_found_msg.lower() in clean_out or
            clean_out == "n/a"):
        return not_found_msg

    citations = format_citations(used_sources)
    if citations and citations not in out:
        return f"{out.strip()} {citations}"
    return out.strip()


