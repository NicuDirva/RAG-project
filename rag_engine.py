class ManualRAG:
    def __init__(self):
        self.chunks: List[Chunk] = []
        self.store = None
        self.embedder = None
        self.llm = None
        self.initialized = False

    def setup(self, pdf_paths: List[str], display_name: Optional[str] = None, device: str = "cpu"):
        print(f"📄 Loading and cleaning PDFs: {pdf_paths}")
        pages = load_and_clean_pdfs(pdf_paths)
        if not pages:
            print("❌ No text extracted from PDFs.")
            return

        print("✂️ Chunking...")
        new_chunks = split_into_chunks(pages, chunk_size=900, overlap_paragraphs=1, min_chunk_chars=50)

        if display_name:
            for c in new_chunks:
                c.source = display_name

        self.chunks.extend(new_chunks)

        print("🔢 Embeddings + Oracle AI Vector Search...")
        self.store, self.embedder = build_vector_store(new_chunks)

        if not self.initialized:
            print("🧠 Loading LLM...")
            self.llm = setup_llm(device=device)
            self.initialized = True
        print(f"✅ Ready. {len(new_chunks)} chunks indexed.")

    def load_from_db(self, device: str = "cpu",
                     model_name: str = "sentence-transformers/paraphrase-multilingual-MiniLM-L12-v2"):
        print("🧠 Se încarcă LLM-ul...")
        self.llm = setup_llm(device=device)

        print("🔢 Se inițializează modelul de Embeddings...")
        self.embedder = SentenceTransformer(model_name)
        dim = self.embedder.get_embedding_dimension()

        user = os.getenv("ORACLE_USER", "rag-project-user")
        password = os.getenv("ORACLE_PASSWORD", "parola_rag")
        dsn = os.getenv("ORACLE_DSN", "172.25.208.1:1522/FREEPDB1")

        self.store = OracleVectorStore(user, password, dsn)
        self.store.ensure_table_exists(dim)

        print("📥 Se preiau documentele existente din Oracle...")
        self.chunks = self.store.get_all_chunks()

        self.initialized = True
        print(f"✅ Gata! Am încărcat {len(self.chunks)} fragmente existente.")

    def ask(self, question: str, top_k: int = 8, min_score: float = 0.0, max_context_chars: int = 3000,
            selected_doc: Optional[str] = None, conversation_history=None) -> str:
        if not self.initialized or not self.chunks:
            return "Please upload and process a document first."

        retrieved = retrieve_context(
            question,
            self.store,
            self.embedder,
            top_k=top_k,
            min_score=max(min_score, 0.20),
            selected_doc=selected_doc
        )

        if not retrieved:
            retrieved = lexical_fallback(question, self.chunks, top_k=min(6, top_k))

        if not retrieved:
            return "Your question is not related to the subject of the provided document."

        answer = answer_question(
            question,
            self.llm,
            retrieved,
            max_context_chars=max_context_chars,
            conversation_history=conversation_history,
        )
        return answer

    def semantic_search(self, query: str, top_k: int = 5) -> List[dict]:
        if not self.initialized or not self.store:
            return []

        retrieved = retrieve_context(query, self.store, self.embedder, top_k=top_k, min_score=0.15)

        if not retrieved:
            retrieved = lexical_fallback(query, self.chunks, top_k=top_k)

        results = []
        for score, chunk in retrieved:
            results.append({
                "doc_name": os.path.basename(chunk.source),
                "score": score,
                "page": chunk.page,
                "text": chunk.text
            })

        return results