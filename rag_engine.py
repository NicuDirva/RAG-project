@dataclass
class Chunk:
    text: str
    source: str
    page: int
    chunk_id: int
    line_number: Optional[int] = None


class OracleVectorStore:
    def __init__(self, user, password, dsn):
        self.user = user
        self.password = password
        self.dsn = dsn
        self.table_name = "document_chunks2"
        self.dim = 384

    def get_connection(self):
        return oracledb.connect(user=self.user, password=self.password, dsn=self.dsn)

    def ensure_table_exists(self, dim: int):
        self.dim = dim
        ddl = f"""
        DECLARE
            e_table_exists EXCEPTION;
            PRAGMA EXCEPTION_INIT(e_table_exists, -955);
            column_exists NUMBER;
        BEGIN
            BEGIN
                EXECUTE IMMEDIATE '
                    CREATE TABLE {self.table_name} (
                        id          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
                        doc_name    VARCHAR2(255),
                        page_number NUMBER,
                        line_number NUMBER,
                        text_chunk  CLOB,
                        embedding   VECTOR({dim}, FLOAT32)
                    ) TABLESPACE USERS
                ';
            EXCEPTION
                WHEN e_table_exists THEN
                    SELECT count(*) INTO column_exists FROM user_tab_cols
                    WHERE table_name = UPPER('{self.table_name}') AND column_name = 'LINE_NUMBER';

                    IF column_exists = 0 THEN
                        EXECUTE IMMEDIATE 'DROP TABLE {self.table_name} CASCADE CONSTRAINTS';
                        EXECUTE IMMEDIATE '
                            CREATE TABLE {self.table_name} (
                                id          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
                                doc_name    VARCHAR2(255),
                                page_number NUMBER,
                                line_number NUMBER,
                                text_chunk  CLOB,
                                embedding   VECTOR({dim}, FLOAT32)
                            ) TABLESPACE USERS
                        ';
                    END IF;
            END;
        EXCEPTION
            WHEN OTHERS THEN RAISE;
        END;
        """

        idx_name = f"{self.table_name}_vidx"
        idx_ddl = f"""
        DECLARE
            e_idx_exists EXCEPTION;
            PRAGMA EXCEPTION_INIT(e_idx_exists, -955);
            e_not_supported EXCEPTION;
            PRAGMA EXCEPTION_INIT(e_not_supported, -40674);
        BEGIN
            EXECUTE IMMEDIATE '
                CREATE VECTOR INDEX {idx_name}
                ON {self.table_name} (embedding)
                ORGANIZATION NEIGHBOR GRAPH
                DISTANCE COSINE
            ';
        EXCEPTION
            WHEN e_idx_exists THEN NULL;
            WHEN e_not_supported THEN
                DBMS_OUTPUT.PUT_LINE('Vector index not supported, skipping.');
            WHEN OTHERS THEN RAISE;
        END;
        """
        print(f"Ensuring table {self.table_name} exists in Oracle...")
        try:
            with self.get_connection() as conn:
                with conn.cursor() as cur:
                    cur.execute(ddl)
                    try:
                        cur.execute(idx_ddl)
                    except Exception as e:
                        print(f"⚠️ Warning creating index: {e}")
                conn.commit()
            print("✅ Oracle table verified.")
        except Exception as e:
            print(f"❌ Error ensuring table exists: {e}")
            raise

    def add_chunks(self, chunks: List[Chunk], embeddings: np.ndarray):
        print(f"Inserting {len(chunks)} chunks into Oracle...")
        try:
            with self.get_connection() as conn:
                with conn.cursor() as cur:
                    sql = f"INSERT INTO {self.table_name} (doc_name, page_number, line_number, text_chunk, embedding) VALUES (:1, :2, :3, :4, :5)"
                    for i, c in enumerate(chunks):
                        vec = embeddings[i].astype(np.float32).tolist()
                        vec_str = "[" + ",".join(map(str, vec)) + "]"
                        cur.execute(sql, [
                            os.path.basename(c.source),
                            c.page,
                            c.line_number or 0,
                            c.text,
                            vec_str
                        ])
                conn.commit()
            print(f"✅ Successfully inserted {len(chunks)} chunks.")
        except Exception as e:
            print(f"❌ Error inserting chunks: {e}")
            raise

    def search(self, query_embedding: np.ndarray, top_k: int = 8, doc_name: Optional[str] = None) -> List[
        Tuple[float, Chunk]]:
        q_emb_str = "[" + ",".join(map(str, query_embedding.tolist())) + "]"
        results = []
        with self.get_connection() as conn:
            with conn.cursor() as cur:
                where_clause = ""
                params = {"q_emb": q_emb_str, "k": top_k}
                if doc_name:
                    where_clause = "WHERE doc_name = :doc_name"
                    params["doc_name"] = os.path.basename(doc_name)

                sql = f"""
                SELECT
                    VECTOR_DISTANCE(embedding, :q_emb, COSINE) as score,
                    doc_name,
                    page_number,
                    line_number,
                    text_chunk
                FROM {self.table_name}
                {where_clause}
                ORDER BY score
                FETCH FIRST :k ROWS ONLY
                """
                cur.execute(sql, **params)
                for i, row in enumerate(cur.fetchall()):
                    score, doc, page, line, text_lob = row
                    text = text_lob.read() if hasattr(text_lob, 'read') else text_lob
                    results.append(
                        (1.0 - float(score), Chunk(text=text, source=doc, page=page, line_number=line, chunk_id=i)))
        return results

    def get_all_chunks(self) -> List[Chunk]:
        chunks = []
        try:
            with self.get_connection() as conn:
                with conn.cursor() as cur:
                    sql = f"SELECT id, doc_name, page_number, line_number, text_chunk FROM {self.table_name}"
                    cur.execute(sql)
                    for row in cur.fetchall():
                        chunk_id, doc, page, line, text_lob = row
                        text = text_lob.read() if hasattr(text_lob, 'read') else text_lob
                        chunks.append(Chunk(
                            text=text,
                            source=doc,
                            page=page,
                            chunk_id=chunk_id,
                            line_number=line
                        ))
        except Exception as e:
            print(f"⚠️ Eroare la extragerea chunk-urilor din DB: {e}")
        return chunks


# --- PREPROCESSING & CHUNKING ---
PAGE_NUM_PATTERNS = [
    r'^\s*\d+\s*$',
    r'^\s*page\s*\d+(\s*of\s*\d+)?\s*$',
    r'^\s*\d+\s+\d+\s*$',
]


def clean_pdf_text(text: str) -> List[Tuple[int, str]]:
    text = text.replace("\r", "\n")
    text = re.sub(r'(\w+)-\n\s*(\w+)', r'\1\2', text)
    cleaned_lines = []
    for i, line in enumerate(text.split("\n"), start=1):
        raw = line.strip()
        if not raw:
            continue
        lower = raw.lower()
        if any(re.match(pat, lower) for pat in PAGE_NUM_PATTERNS):
            continue
        if re.match(r'^[_\-–—=]{5,}$', raw):
            continue
        raw = re.sub(r'[ \t]+', ' ', raw)
        cleaned_lines.append((i, raw))
    return cleaned_lines


def load_and_clean_pdfs(pdf_paths: List[str], min_chars_per_page: int = 10) -> List[Tuple[str, int, int, str]]:
    all_lines = []
    for path in pdf_paths:
        with pdfplumber.open(path) as pdf:
            for i, page in enumerate(pdf.pages, start=1):
                text = page.extract_text(layout=True) or page.extract_text() or ""
                lines = clean_pdf_text(text)
                total_text = " ".join(l[1] for l in lines)
                if total_text and len(total_text) >= min_chars_per_page:
                    for line_num, line_text in lines:
                        all_lines.append((path, i, line_num, line_text))
    return all_lines


def split_into_chunks(
        lines: List[Tuple[str, int, int, str]],
        chunk_size: int = 900,
        overlap_chars: int = 150,
        min_chunk_chars: int = 50,
) -> List[Chunk]:
    chunks: List[Chunk] = []
    chunk_id = 0
    if not lines:
        return []

    # Grupăm liniile pe pagini și documente
    pages_map = {}
    for source, page_num, line_num, text in lines:
        key = (source, page_num)
        if key not in pages_map:
            pages_map[key] = []
        pages_map[key].append((line_num, text))

    for (source, page_num), page_lines in pages_map.items():
        buf_lines: List[Tuple[int, str]] = []

        def flush_buffer():
            nonlocal chunk_id
            if not buf_lines:
                return
            buf_text = "\n".join(l[1] for l in buf_lines).strip()
            if len(buf_text) >= min_chunk_chars:
                start_line = buf_lines[0][0]
                chunks.append(
                    Chunk(text=buf_text, source=source, page=page_num, line_number=start_line, chunk_id=chunk_id))
                chunk_id += 1

        for line_num, line_text in page_lines:
            current_buf_text = "\n".join(l[1] for l in buf_lines + [(line_num, line_text)])

            # Când depășim 900 de caractere
            if buf_lines and len(current_buf_text) > chunk_size:
                flush_buffer()  # Salvăm chunk-ul în baza de date

                tail = []
                tail_len = 0
                # Mergem de la ultima linie înapoi și adunăm text
                for l in reversed(buf_lines):
                    tail.insert(0, l)  # Adăugăm linia la începutul listei tail
                    tail_len += len(l[1]) + 1  # +1 pentru caracterul de linie nouă (\n)

                    # Ne oprim când am adunat destule caractere pentru overlap
                    if tail_len >= overlap_chars:
                        break
                # ------------------------------

                # Începem noul chunk cu coada de ~150 caractere + linia nouă
                buf_lines = tail + [(line_num, line_text)]
            else:
                buf_lines.append((line_num, line_text))

        # Salvăm ce a rămas la finalul paginii
        flush_buffer()

    # Eliminăm posibilele duplicate exacte (dacă PDF-ul are elemente repetitive)
    seen = set()
    uniq: List[Chunk] = []
    for c in chunks:
        key = (c.text, c.source, c.page, c.line_number)
        if key not in seen:
            seen.add(key)
            uniq.append(c)

    return uniq

